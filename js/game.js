//Todas as questões
var pontuacaoAtual = 0
function verificar1() {
    let acertou1 = document.getElementById("correta1").checked
    

    if (acertou1 == true) {
        document.getElementById("acertou1").textContent = "Parabéns duelista, você acertou!!"
        document.getElementById("acertou1").style.color = "yellow"

        pontuacaoAtual += 10

        document.getElementById("score").textContent = pontuacaoAtual
        console.log(`Pontuação atual ${pontuacaoAtual}`)
    }else{
        document.getElementById("acertou1").textContent = "Que pena duelista, você errou..."
        document.getElementById("acertou1").style.color = "red"
    } 
}

